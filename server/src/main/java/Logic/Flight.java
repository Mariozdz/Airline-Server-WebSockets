package Logic;


public class Flight {

  private int id;
  private int leave;
  private int arrive;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getLeave() {
    return leave;
  }

  public void setLeave(int leave) {
    this.leave = leave;
  }


  public int getArrive() {
    return arrive;
  }

  public void setArrive(int arrive) {
    this.arrive = arrive;
  }

}
