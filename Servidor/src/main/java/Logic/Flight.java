package Logic;


public class Flight {

  private String id;
  private String leave;
  private String arrive;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getLeave() {
    return leave;
  }

  public void setLeave(String leave) {
    this.leave = leave;
  }


  public String getArrive() {
    return arrive;
  }

  public void setArrive(String arrive) {
    this.arrive = arrive;
  }

}
